private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
// TODO add your handling code here:
    tr_id.setEditable(false);
    c_name.setEditable(false);
    balance.setEditable(false);
    try
    {
        Connect.getConnection();
        String query = "select count(*) from transaction1";
        ResultSet rs = Connect.st.executeQuery(query);
        rs.first();
        int count = rs.getInt(1);
        rs.close();
        if(count==0)
        {
            tr_id.setText(String.valueOf(count+100));
        }
        else
        {
            String query1 = "select transid from transaction1";
            rs = Connect.st.executeQuery(query1);
            rs.last();
            int i = Integer.parseInt(rs.getString(1));
            i=i+1;
            tr_id.setText(String.valueOf(i));
            
        }
        date=DateToday.getDate();
        dateField.setText(DateToday.day+"-"+DateToday.monthName+"-"+DateToday.year);
    }
    catch(Exception e)
    {
        System.out.println(e);
    }

}//GEN-LAST:event_formInternalFrameOpened

