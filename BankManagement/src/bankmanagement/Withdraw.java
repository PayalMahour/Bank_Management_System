private void acc_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_acc_idKeyReleased
// TODO add your handling code here:
    if(!acc_id.getText().trim().equals(""))
     {
       try
       {
        
        Connect.getConnection();
        String query = "select name,balance from new_account where account='"+acc_id.getText()+"'";
        ResultSet rs = Connect.st.executeQuery(query);
        if(rs.next())
        {
            label.setText("valid Account ID");
            c_name.setText(rs.getString("name"));
            balance.setText(rs.getString("balance"));
        } 
        else
        {
        label.setText("invalid account ID");    
        c_name.setText("");
        balance.setText("");
        }
    }   
        catch(Exception e)
        {
            System.out.println(e);
        }
     }
    else
    {
       label.setText("Enter account Id");
    }
}//GEN-LAST:event_acc_idKeyReleased

private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// TODO add your handling code here:
   
    try
    {
        if(!acc_id.getText().trim().equals(""))
    {
        Connect.getConnection();
        String query = "select name,balance from new_account where account='"+acc_id.getText()+"'";
        ResultSet rs = Connect.st.executeQuery(query);
        if(!rs.next())
        {
            JOptionPane.showMessageDialog(rootPane,"Enter correct Account ID");
            return;
        }
    }
       else
        {
            JOptionPane.showMessageDialog(rootPane,"Enter Account Id for the transaction");
            return;
         }
        
    }
    catch(Exception e)
    {
        System.out.println(e);
    }
    
    if(withdraw.getText().equals(""))
    {
        JOptionPane.showMessageDialog(rootPane,"Enter the amount to be withdrawn ");
        return;
    }
    else if(withdraw.getText().length()<3)
    {
        JOptionPane.showMessageDialog(rootPane,"Minimum limit to withdraw money is Rs.100 ");
        withdraw.setText("");
        return;
    }
    
    int b = Integer.parseInt(balance.getText());
    int d = Integer.parseInt(withdraw.getText());
    b=b-d;
    if(b>=1000)
    {
    try
    {
        String query = "update new_account set balance='"+String.valueOf(b)+"',withdraw_date='"+date+"',"
          + "last_withdraw='"+String.valueOf(d)+"' where account='"+acc_id.getText()+"'";                
           
       int row = Connect.st.executeUpdate(query);
       if(row>0)
       {
           JOptionPane.showMessageDialog(rootPane,"Rs."+d+" has been withdrawn\nNow your Account balnce is Rs."+b);
          
       }
       String query1 = "insert into transaction1 (transid,account,action,date,amount) values "
               + "('"+tr_id.getText()+"','"+acc_id.getText().trim()+"','Withdraw',"
               + "'"+date+"',"
               + "'"+String.valueOf(d)+"')";
       int row1 = Connect.st.executeUpdate(query1);
       if(row1>0)
       {
           JOptionPane.showMessageDialog(rootPane,"Transaction Successfull");
          dispose();
       }    
    }
    catch(Exception e)
    {
        System.out.println(e);
    }
    }
    else
    {
        JOptionPane.showMessageDialog(rootPane,"Not sufficient balance in Account\nYou have to maintain min. balance of Rs1000 in your Account");
        withdraw.setText("");
        return;
    }
      
}//GEN-LAST:event_jButton2ActionPerformed

