private void acc_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_acc_idKeyReleased
// TODO add your handling code here:
     if(!acc_id.getText().trim().equals(""))
     {
       try
       {
        
        Connect.getConnection();
        String query = "select name,balance from new_account where account='"+acc_id.getText()+"'";
        ResultSet rs1 = Connect.st.executeQuery(query);
        if(rs1.next())
        {
            label.setText("valid Account ID");
            c_name.setText(rs1.getString("name"));
            balance.setText(rs1.getString("balance"));
        } 
        else
        {
        label.setText("invalid account ID");    
        c_name.setText("");
        balance.setText("");
        }
        rs1.close();
    }   
        catch(Exception e)
        {
            System.out.println(e);
        }
     }
    else
    {
       label.setText("Enter account Id");
    }  
}//GEN-LAST:event_acc_idKeyReleased

