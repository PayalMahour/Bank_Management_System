private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
// TODO add your handling code here:
    
    
    try
    {
        if(!acc_id.getText().trim().equals(""))
    {
        Connect.getConnection();
        String query1 = "select name,balance from new_account where account='"+acc_id.getText()+"'";
        ResultSet rs2 = Connect.st.executeQuery(query1);
        if(rs2.next())
           {
            c_name.setText(rs2.getString("name"));
            balance.setText(rs2.getString("balance"));
           }
            else 
            {
            JOptionPane.showMessageDialog(rootPane,"Enter correct Account ID");
            acc_id.setText("");
            label.setText("");
            return;
            }
           rs2.close();
    }
       else
        {
            JOptionPane.showMessageDialog(rootPane,"Enter Account Id");
            return;
         }
        while(table.getRowCount()>0)
        {
            ((DefaultTableModel)table.getModel()).removeRow(0);
        }
        
      String query2 ="select transid,action,date,amount from transaction1 where account='"+acc_id.getText()+"'";
      ResultSet rs3 = Connect.st.executeQuery(query2);
      col = rs3.getMetaData().getColumnCount();
      while(rs3.next())
      {
           
            Object []row = new Object[col];
            for(int i=1;i<=col;i++)
            {
                row[i-1]=rs3.getObject(i);
            }
            ((DefaultTableModel)table.getModel()).insertRow(rs3.getRow()-1,row);
        }
      
      rs3.close();
       
      
      }    
    catch(Exception e)
    {
        System.out.println(e);
    }
    btn.setEnabled(false);
    
   
   
}//GEN-LAST:event_btnActionPerformed

